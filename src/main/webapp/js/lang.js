function loadlanguage(target) {
	for (var i = 0; i < target.length; i++) {
		if (target[i].type == "text") $(target[i].class).text(target[i].text);
		if (target[i].type == "placeholder") $(target[i].class).attr("placeholder", target[i].text);
	}
}
$(".flag.english").click(function(){
	loadlanguage(langbase.english);
	$(this).siblings().removeClass("active");
	$(this).addClass("active");
	$.cookie('lang', 'english', { expires: 365, path: '/' });
});
