function checkLanguage() {
    var lang = $.cookie("lang");
    if (lang == undefined) {
        $.cookie('lang', 'english', {expires: 365, path: '/'});
        lang = "english";
    }
    if (lang == "english") $(".flag.english").click();
    if (lang == "spanish") $(".flag.spanish").click();
    if (lang == "hindi") $(".flag.hindi").click();
    if (lang == "french") $(".flag.french").click();
}
checkLanguage();