package com.bean;

public class AlbumBean {

	private String albumId;
	private String albumName;
	private String albumLink;
	private String albumDescription;
	private String photographerId;
	private String photographersFirstName;
	private String photographersLastName;

	public String getAlbumId() {
		return albumId;
	}

	public void setAlbumId(String albumId) {
		this.albumId = albumId;
	}

	public String getPhotographersFirstName() {
		return photographersFirstName;
	}

	public void setPhotographersFirstName(String photographersFirstName) {
		this.photographersFirstName = photographersFirstName;
	}

	public String getPhotographersLastName() {
		return photographersLastName;
	}

	public void setPhotographersLastName(String photographersLastName) {
		this.photographersLastName = photographersLastName;
	}

	public String getAlbumName() {
		return albumName;
	}

	public void setAlbumName(String albumName) {
		this.albumName = albumName;
	}

	public String getAlbumLink() {
		return albumLink;
	}

	public void setAlbumLink(String albumLink) {
		this.albumLink = albumLink;
	}

	public String getAlbumDescription() {
		return albumDescription;
	}

	public void setAlbumDescription(String albumDescription) {
		this.albumDescription = albumDescription;
	}

	public String getPhotographerId() {
		return photographerId;
	}

	public void setPhotographerId(String photographerId) {
		this.photographerId = photographerId;
	}

	

}
